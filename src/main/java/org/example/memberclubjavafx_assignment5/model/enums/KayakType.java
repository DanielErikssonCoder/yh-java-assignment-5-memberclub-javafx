/**
 * Represents different types of kayaks.
 */
public enum KayakType {

    // Open deck, easy to get on and off
    SIT_ON_TOP,

    // Enclosed cockpit, better for cold water
    SIT_INSIDE,

    // Portable and easy storage
    INFLATABLE,

    // Specialized for fishing with rod holders
    FISHING
}

