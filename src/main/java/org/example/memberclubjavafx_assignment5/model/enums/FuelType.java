/**
 * Represents fuel types for camping stoves and boats.
 */
public enum FuelType {

    // Camping stove fuels

    // Methylated spirits (trangia)
    ALCOHOL,

    // Butane/Propane (trangia, lanterns)
    GAS,

    // Multiple fuel types (trangia)
    MULTIFUEL,

    // Boat fuels

    // Petrol (motor boats)
    GASOLINE,

    // Diesel fuel (motor boats)
    DIESEL
}
